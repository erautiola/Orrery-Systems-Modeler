/* ============================================================================
 * sql-export.js — Generate SQL DDL (CREATE TABLE + FK constraints) from the
 * model's database tables (type "dbtable") and foreign-key relationships.
 * ==========================================================================*/
(function (root) {
  "use strict";

  // common SQL reserved words — quoted even though they match the safe pattern
  const RESERVED = new Set(("order user group table select from where index key primary " +
    "foreign references constraint default check unique null not column values insert update " +
    "delete create alter drop join on as by desc asc limit offset case when then end grant " +
    "view trigger sequence schema database int integer date time timestamp text").split(" "));

  // bare identifier if it's a safe, non-reserved SQL name, else double-quoted
  function id(s) {
    s = String(s == null ? "" : s);
    const safe = /^[A-Za-z_][A-Za-z0-9_]*$/.test(s) && !RESERVED.has(s.toLowerCase());
    return safe ? s : '"' + s.replace(/"/g, '""') + '"';
  }

  function toSql(model) {
    const tables = (model.elements || []).filter((e) => e.type === "dbtable");
    if (!tables.length) return "-- No database tables in this model.\n";

    const out = [`-- SQL DDL generated by Orrery Systems Modeler`, `-- Model: ${model.name || "model"}`, ""];

    for (const t of tables) {
      const cols = t.columns || [];
      out.push(`CREATE TABLE ${id(t.name)} (`);
      const defs = cols.map((c) => {
        let d = `  ${id(c.name)} ${c.dataType || "TEXT"}`;
        if (c.nullable === false) d += " NOT NULL";
        if (c.unique && !c.pk) d += " UNIQUE";
        if (c.defaultValue) d += ` DEFAULT ${c.defaultValue}`;
        return d;
      });
      const pks = cols.filter((c) => c.pk).map((c) => id(c.name));
      if (pks.length) defs.push(`  PRIMARY KEY (${pks.join(", ")})`);
      out.push(defs.join(",\n"));
      out.push(");", "");
    }

    // foreign-key constraints
    const byId = new Map(tables.map((t) => [t.id, t]));
    let n = 0;
    for (const r of (model.relationships || [])) {
      if (r.type !== "fk") continue;
      const child = byId.get(r.sourceId), parent = byId.get(r.targetId);
      if (!child || !parent) continue;
      const parentPk = (parent.columns || []).find((c) => c.pk);
      const fkCol = r.fkColumn || (parent.name.toLowerCase() + "_id");
      const refCol = r.refColumn || (parentPk && parentPk.name) || "id";
      out.push(
        `ALTER TABLE ${id(child.name)} ADD CONSTRAINT ${id("fk_" + child.name + "_" + (++n))} ` +
        `FOREIGN KEY (${id(fkCol)}) REFERENCES ${id(parent.name)} (${id(refCol)});`
      );
    }
    return out.join("\n") + "\n";
  }

  const api = { toSql };
  if (typeof module !== "undefined" && module.exports) module.exports = api; // Node (tests)
  if (root) root.SqlExport = api;                                            // browser
})(typeof window !== "undefined" ? window : null);
